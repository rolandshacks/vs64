class Lang {

    static getEnumKey(enumDef, enumValue) {
        for (const element of Object.entries(enumDef)) {
            if (element[1] == enumValue) {
                return element[0];
            }
        }

        return null;
    }
}

//-----------------------------------------------------------------------------------------------//
// Module Exports
//-----------------------------------------------------------------------------------------------//

module.exports = {
    Lang: Lang
};
